    %map(36,64)=4;
    %60
    %map(33,59)=4;
    %map(33,58)=4;
    %90
    %map(32,52)=4;
    %map(32,51)=4;
    %120
    %map(33,43)=4;
    %map(33,44)=4;
    %135
    %map(37,37)=4;
    %map(36,38)=4;
    %150 
    %map(43,33)=4;
    %map(44,33)=4;
    %180
    %map(51,32) = 4;
    %map(52,32) = 4;
    %-30
    %map(58,69)=4;
    %map(59,69)=4;
    %-45
    %map(65,65)=4;
    %map(66,64)=4;
    %-60
    %map(69,59)=4;
    %map(69,58)=4;
    %-90 
